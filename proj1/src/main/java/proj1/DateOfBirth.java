package proj1;

public class DateOfBirth {
    // Fields
    private int month;
    private int day;
    private int year;

    // Constructor with no params
    public DateOfBirth() {}

    // Getters and setters for each field

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }
}
